/**
 * Dataset contract version.
 *
 * Why we version:
 * - UI and BFF can evolve independently.
 * - We can reject incompatible clients early with a clear error.
 */
export const CONTRACT_VERSION = 'v1' as const;

export type ContractVersion = typeof CONTRACT_VERSION;

/**
 * Dataset identifier.
 *
 * Think of this as "which dataset to query" (like an API resource name),
 * not "which widget". Widgets *choose* a datasetId.
 *
 * Examples:
 * - "payment.kpi"
 * - "payment.timeseriesDaily"
 */
export type DatasetId = string;

// Canonical home: @dashboard-builder/platform-core
// Imported for local use and re-exported for backward compatibility with existing consumers.
import type { JsonPrimitive, JsonValue } from '@dashboard-builder/platform-core';
export type { JsonPrimitive, JsonValue };

/**
 * DatasetQuery is the ONLY input format the UI sends to the BFF.
 *
 * Important design goals:
 * - **Stable**: widgets only know this shape, not SQL, not Oracle, not Cube.
 * - **Cache-friendly**: can be used to build cache keys.
 * - **Portable**: the server can compile this into different backends.
 */
export type DatasetQueryV1 = {
	contractVersion: ContractVersion;
	/**
	 * Optional request identifier for tracing/dedup/debug.
	 * Should be generated by the caller; server may echo it back.
	 */
	requestId?: string;
	/**
	 * Global filters snapshot (typically produced by `entities/filter`).
	 *
	 * @deprecated Target wire contract uses flat `params` only.
	 * Legacy filter bags should be adapted into flat `params` before transport.
	 * New datasets and migrated pages should use `params` directly.
	 * Will be removed after filter migration (BR-5).
	 */
	filters?: Record<string, JsonValue>;
	/**
	 * Dataset-specific parameters (widget config etc).
	 * This is the canonical flat wire bag — planner output and page/widget
	 * params are merged client-side before transport.
	 */
	params?: Record<string, JsonValue>;
};

export type DatasetQuery = DatasetQueryV1;

export type DatasetFieldType =
	| 'string'
	| 'number'
	| 'boolean'
	| 'date'
	| 'datetime'
	| 'json'
	| 'unknown';

export type DatasetField = {
	name: string;
	type: DatasetFieldType;
	nullable?: boolean;
};

/**
 * DatasetResponse is the ONLY output format the BFF returns to the UI.
 *
 * `rows` is intentionally generic (Record<string, JsonValue>) so:
 * - providers can return what makes sense for the dataset
 * - UI can map it into view-models (charts/tables)
 *
 * Later you can tighten this per-dataset by adding a registry layer,
 * but for MVP this keeps wiring simple.
 */
export type DatasetResponseV1 = {
	contractVersion: ContractVersion;
	datasetId: DatasetId;
	requestId?: string;
	fields: DatasetField[];
	rows: Array<Record<string, JsonValue>>;
	meta?: {
		executedAt?: string;
		tenantId?: string;
		/** @deprecated Use `sourceKind` instead. Will be removed after migration. */
		source?: 'mock' | 'oracle' | 'postgres' | 'cube' | 'clickhouse' | 'unknown';
		limit?: number;
		offset?: number;
		sort?: Array<{
			field: string;
			dir: 'asc' | 'desc';
		}>;
		/** Total row count when the backend can provide it cheaply. */
		totalCount?: number;
		/** Backend that actually served the response (for observability). */
		sourceKind?: string;
		/** Age of the cached value in ms; 0 or omitted for fresh responses. */
		cacheAgeMs?: number;
	};
};

export type DatasetResponse = DatasetResponseV1;

// ---------------------------------------------------------------------------
// DatasetError — stable JSON error shape for dataset operations
// ---------------------------------------------------------------------------

export type DatasetErrorCode =
	| 'DATASET_NOT_FOUND'
	| 'DATASET_ACCESS_DENIED'
	| 'DATASET_INVALID_PARAMS'
	| 'UNSUPPORTED_BACKEND'
	| 'DATASET_EXECUTION_FAILED'
	| 'DATASET_TIMEOUT'
	| 'DATASET_CONNECTION_ERROR';

export type DatasetError = {
	error: string;
	code: DatasetErrorCode;
	retryable: boolean;
	requestId?: string;
};
